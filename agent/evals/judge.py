"""
LLM-as-judge evaluator for synthesis report quality.

Returns 5 dimension scores in a single LLM call per item.
Used as an inline evaluator with Langfuse run_experiment().

Judge prompt: stored in Langfuse as "first-light-eval-judge" (with hardcoded fallback).
"""

import json
import logging
import re
from typing import Any, Dict, List, Optional

from langfuse.experiment import Evaluation

from agent.evals.config import JUDGE_MODEL, JUDGE_TEMPERATURE
from agent.evals.synthesis_replayer import _completion_with_retry
from agent.langfuse_integration import get_agent_prompt_with_fallback
from agent.model_config import get_model_config

logger = logging.getLogger(__name__)

_JUDGE_PROMPT_FALLBACK = """\
You are an expert evaluator for network security daily reports generated by the
First Light AI system. Score the CANDIDATE REPORT on 5 dimensions.

Return ONLY this JSON, no markdown:
{
  "completeness": float,
  "false_positive_rate": float,
  "actionability": float,
  "format": float,
  "severity_accuracy": float,
  "notes": string
}

Scoring:
- completeness (0.0-1.0): Each domain with critical/warning findings must appear in the report. Missing a critical finding = 0.
- false_positive_rate (0.0-1.0): 1.0 = no false alarms. Each "suspicious"/"critical" mention for an ok/info finding = -0.15.
- actionability (0.0-1.0): "Monitor" alone is not actionable. Specific commands, thresholds, or next steps are actionable.
- format (0.0-1.0): Expect per-domain sections, headline severity, executive summary, and a recommendations block.
- severity_accuracy (0.0-1.0): If input has 2 critical findings, a WARNING headline = 0.5. Headline must match data.

The REFERENCE REPORT is for orientation only. A better candidate report is valid."""

DIMENSIONS = [
    "completeness",
    "actionability",
    "severity_accuracy",
    "format",
    "false_positive_rate",
]


def _get_judge_prompt() -> str:
    """Fetch judge prompt from Langfuse, falling back to hardcoded version."""
    return get_agent_prompt_with_fallback(
        "first-light-eval-judge",
        _JUDGE_PROMPT_FALLBACK,
    )


def _parse_judge_response(raw: str) -> Optional[Dict[str, float]]:
    """Parse the judge's JSON response, stripping markdown fences if present."""
    cleaned = re.sub(r"^```(?:json)?\s*|\s*```$", "", raw.strip(), flags=re.MULTILINE | re.IGNORECASE).strip()
    try:
        data = json.loads(cleaned)
        if not isinstance(data, dict):
            return None
        scores = {}
        for dim in DIMENSIONS:
            val = data.get(dim)
            if val is not None:
                scores[dim] = max(0.0, min(1.0, float(val)))
        if len(scores) < len(DIMENSIONS):
            logger.warning("Judge returned only %d/%d dimensions", len(scores), len(DIMENSIONS))
            return None
        return scores
    except (json.JSONDecodeError, ValueError, TypeError) as e:
        logger.error("Failed to parse judge response: %s — raw: %s", e, raw[:200])
        return None


def make_judge_evaluator():
    """Create a Langfuse EvaluatorFunction that judges synthesis quality.

    Returns a callable with signature:
        def evaluator(*, input, output, expected_output, metadata, **kwargs)
            -> List[Evaluation]
    """
    model_config = get_model_config()
    judge_system_prompt = _get_judge_prompt()

    def judge_evaluator(
        *,
        input: Any,
        output: Any,
        expected_output: Any = None,
        metadata: Optional[Dict[str, Any]] = None,
        **kwargs,
    ) -> List[Evaluation]:
        candidate_report = str(output) if output else ""
        reference_report = str(expected_output) if expected_output else "No reference available."

        # Build the domain summaries context from the input
        if isinstance(input, dict):
            user_msg = input.get("synthesis_user_message", "")
        else:
            user_msg = getattr(input, "synthesis_user_message", "") or ""

        user_prompt = (
            f"## DOMAIN AGENT INPUT (what the synthesis model received)\n\n"
            f"{user_msg[:20000]}\n\n"
            f"---\n\n"
            f"## REFERENCE REPORT (actual production output)\n\n"
            f"{reference_report[:10000]}\n\n"
            f"---\n\n"
            f"## CANDIDATE REPORT (to evaluate)\n\n"
            f"{candidate_report[:15000]}"
        )

        messages = [
            {"role": "system", "content": judge_system_prompt},
            {"role": "user", "content": user_prompt},
        ]

        try:
            raw = _completion_with_retry(
                model=JUDGE_MODEL,
                messages=messages,
                temperature=JUDGE_TEMPERATURE,
                api_base=model_config.litellm_base_url,
                api_key=model_config.litellm_api_key,
            )
        except Exception as e:
            logger.error("Judge LLM call failed: %s", e)
            return [
                Evaluation(name=dim, value=0.0, comment=f"Judge call failed: {e}")
                for dim in DIMENSIONS
            ]

        scores = _parse_judge_response(raw)
        if scores is None:
            return [
                Evaluation(name=dim, value=0.0, comment="Judge returned unparseable response")
                for dim in DIMENSIONS
            ]

        notes = ""
        try:
            data = json.loads(re.sub(r"^```(?:json)?\s*|\s*```$", "", raw.strip(), flags=re.MULTILINE | re.IGNORECASE).strip())
            notes = data.get("notes", "")
        except Exception:
            pass

        evaluations = []
        for dim in DIMENSIONS:
            val = scores[dim]
            comment = notes if val < 0.7 else None
            evaluations.append(Evaluation(name=dim, value=val, comment=comment))

        return evaluations

    return judge_evaluator
